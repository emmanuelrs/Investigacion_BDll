    public void registrar(){
       String password = String.valueOf(tfPassword.getPassword());
       Neo4jConnection usuario = new Neo4jConnection();
       usuario.conectar();
       if(!usuario.verificaUsuario(tfNombreUsuario.getText())){
           usuario.addGamer(tfNombre.getText(), tfApellido.getText(),tfCorreo.getText(), tfNombreUsuario.getText(),password);
           usuario.desconectar();
           MailSender ms = new MailSender();
            ms.enviaCorreo(tfNombreUsuario.getText(),tfCorreo.getText());
           JOptionPane.showMessageDialog(null, "Usuario Agregado");
           dispose();
       }
       else{
           JOptionPane.showMessageDialog(null, "El usuario ya existe!");
           usuario.desconectar();
           
       } 
    }
